// simple grade calculator using if/else (e.g., A, B, C, F based on marks)
let marks = 80; // Change this value to test different cases
if (marks >= 90) {
    console.log("Grade: A");
}
 else if (marks >= 80) {
    console.log("Grade: B");
}
 else if (marks >= 70) {
console.log("Grade: C");
}
 else if (marks >= 60) {
    console.log("Grade: D");
}
 else {
    console.log("Grade: F");
}